package Arrays;

import java.util.Arrays;



public class Swap{
    public static void main(String[] args) {
        int a = 10, b = 20;
        System.out.println("Before Swapping Values are: " + a + "  " +b);


        int temp = a;
        a = b;
        b = temp;

        System.out.println("Before Swapping Values are: " + a + "  "  + b);


    }
}







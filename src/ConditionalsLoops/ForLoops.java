package ConditionalsLoops;

public class ForLoops{
    public static void main(String[] args) {

        /*
         Syntax of For Loop

         for(initialization; condition; increment/decrement)

         */

        //Print numbers from 1-5

        for(int num = 1; num <= 5; num++){
            System.out.println(num);
        }
    }
}
